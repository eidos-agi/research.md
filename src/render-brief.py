#!/usr/bin/env python3
"""
render-brief.py — Convert a research BRIEF.md to a branded PDF.

Standalone renderer for research.md. No shared code with other pipelines.
Learns from render_adr.py patterns but is independently maintained.

Usage:
    python3 render-brief.py BRIEF.md
    python3 render-brief.py BRIEF.md --brand connection-forge
    python3 render-brief.py BRIEF.md --brand-color "#193B2D" --brand-name "AIC Holdings"
    python3 render-brief.py BRIEF.md --logo logo.png -o output.pdf

Brand presets:
    --brand connection-forge   → AIC green, "Connection Forge" footer
    --brand aic                → AIC blue, "AIC Holdings" footer
    --brand research           → Neutral gray, "research.md" footer
"""

