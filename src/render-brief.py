def inline_markup(text, brand_hex="#37474F"):
    """Convert inline markdown to reportlab XML markup."""
    text = re.sub(r'\*\*\*(.+?)\*\*\*', r'<b><i>\1</i></b>', text)
    text = re.sub(r'\*\*(.+?)\*\*', r'<b>\1</b>', text)
    text = re.sub(r'\*(.+?)\*', r'<i>\1</i>', text)
    text = re.sub(r'`([^`]+)`', rf'<font face="Courier" size="8" color="{brand_hex}">\1</font>', text)
    text = re.sub(r'\[([^\]]+)\]\([^)]+\)', r'<u>\1</u>', text)
    # Escape XML then restore tags
    text = text.replace('&', '&amp;').replace('<', '&lt;').replace('>', '&gt;')
    for tag in ['b', 'i', 'u', 'font']:
        text = text.replace(f'&lt;{tag}&gt;', f'<{tag}>')
        text = text.replace(f'&lt;{tag} ', f'<{tag} ')
        text = text.replace(f'&lt;/{tag}&gt;', f'</{tag}>')
    text = re.sub(r'<font([^&]*?)&gt;', r'<font\1>', text)
    return text


def parse_markdown(md_text):
    """Parse markdown into block tokens."""
    blocks = []
    lines = md_text.split('\n')
    i = 0

    while i < len(lines):
        line = lines[i]

        if not line.strip():
            i += 1
            continue

        if re.match(r'^---+\s*$', line):
            blocks.append({'type': 'hr'})
            i += 1
            continue

        m = re.match(r'^(#{1,4})\s+(.*)', line)
        if m:
            blocks.append({'type': f'h{len(m.group(1))}', 'text': m.group(2).strip()})
            i += 1
            continue

        if re.match(r'^>\s+', line):
            quote_lines = []
            while i < len(lines) and (lines[i].startswith('>') or (lines[i].strip() and quote_lines)):
                quote_lines.append(re.sub(r'^>\s*', '', lines[i]))
                i += 1
            blocks.append({'type': 'blockquote', 'text': ' '.join(quote_lines)})
            continue

        if '|' in line and line.strip().startswith('|'):
            table_lines = []
            while i < len(lines) and '|' in lines[i] and lines[i].strip().startswith('|'):
                table_lines.append(lines[i])
                i += 1
            blocks.append({'type': 'table', 'lines': table_lines})
            continue

        if line.strip().startswith('```'):
            code_lines = []
            i += 1
            while i < len(lines) and not lines[i].strip().startswith('```'):
                code_lines.append(lines[i])
                i += 1
            i += 1  # skip closing ```
            blocks.append({'type': 'code', 'text': '\n'.join(code_lines)})
            continue

        m = re.match(r'^(\s*)[-*]\s+(.*)', line)
        if m:
            items = []
            while i < len(lines) and re.match(r'^(\s*)[-*]\s+(.*)', lines[i]):
                bm = re.match(r'^(\s*)[-*]\s+(.*)', lines[i])
                items.append(bm.group(2).strip())
                i += 1
            blocks.append({'type': 'bullets', 'items': items})
            continue

        m = re.match(r'^(\s*)\d+\.\s+(.*)', line)
        if m:
            items = []
            while i < len(lines) and re.match(r'^(\s*)\d+\.\s+(.*)', lines[i]):
                nm = re.match(r'^(\s*)\d+\.\s+(.*)', lines[i])
                items.append(nm.group(2).strip())
                i += 1
            blocks.append({'type': 'numbered', 'items': items})
            continue

        para_lines = []
        while i < len(lines) and lines[i].strip() and not lines[i].startswith('#') \
                and not lines[i].startswith('|') and not re.match(r'^---', lines[i]) \
                and not re.match(r'^[-*]\s+', lines[i]) and not re.match(r'^\d+\.\s+', lines[i]) \
                and not lines[i].startswith('>') and not lines[i].strip().startswith('```'):
            para_lines.append(lines[i])
            i += 1
        if para_lines:
            text = ' '.join(para_lines)
            # Detect verdict lines
            if text.startswith('**Verdict:**'):
                blocks.append({'type': 'verdict', 'text': text})
            else:
                blocks.append({'type': 'paragraph', 'text': text})

    return blocks


def parse_table(table_lines):
    rows = []
    for line in table_lines:
        cells = [c.strip() for c in line.strip().strip('|').split('|')]
        rows.append(cells)
    if len(rows) < 2:
        return rows, False
    sep = rows[1]
    if all(re.match(r'^[-:]+$', c.strip()) for c in sep if c.strip()):
        return [rows[0]] + rows[2:], True
    return rows, False


def build_pdf_table(table_lines, styles, brand):
    rows, has_header = parse_table(table_lines)
    if not rows:
        return None

    num_cols = max(len(r) for r in rows)
    rows = [r + [''] * (num_cols - len(r)) for r in rows]

    brand_hex = brand["primary"].hexval() if hasattr(brand["primary"], 'hexval') else "#37474F"

    data = []
    for ri, row in enumerate(rows):
        pdf_row = []
        for cell in row:
            style = styles['Brief_TableHeader'] if (ri == 0 and has_header) else styles['Brief_TableCell']
            pdf_row.append(Paragraph(inline_markup(cell, brand_hex), style))
        data.append(pdf_row)

    avail = letter[0] - 2 * PAGE_MARGIN
    col_width = avail / num_cols

    t = Table(data, colWidths=[col_width] * num_cols)

    style_cmds = [
        ('VALIGN', (0, 0), (-1, -1), 'TOP'),
        ('TOPPADDING', (0, 0), (-1, -1), 4),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 4),
        ('LEFTPADDING', (0, 0), (-1, -1), 6),
        ('RIGHTPADDING', (0, 0), (-1, -1), 6),
        ('GRID', (0, 0), (-1, -1), 0.5, brand["border"]),
    ]

    if has_header:
        style_cmds += [
            ('BACKGROUND', (0, 0), (-1, 0), brand["primary"]),
            ('TEXTCOLOR', (0, 0), (-1, 0), colors.white),
        ]

    for ri in range(1 if has_header else 0, len(data)):
        if ri % 2 == 0:
            style_cmds.append(('BACKGROUND', (0, ri), (-1, ri), brand["primary_light"]))

    t.setStyle(TableStyle(style_cmds))
    return t


def header_footer(canvas, doc, brand, logo_path=None):
    canvas.saveState()
    width, height = letter

    canvas.setStrokeColor(brand["primary"])
    canvas.setLineWidth(2)
    canvas.line(PAGE_MARGIN, height - 0.5 * inch, width - PAGE_MARGIN, height - 0.5 * inch)

    if logo_path and os.path.exists(logo_path):
        try:
            canvas.drawImage(logo_path, PAGE_MARGIN, height - 0.48 * inch,
                             width=1.2 * inch, height=0.35 * inch,
                             preserveAspectRatio=True, mask='auto')
        except Exception:
            pass
    else:
        canvas.setFont('Helvetica-Bold', 9)
        canvas.setFillColor(brand["primary"])
        canvas.drawString(PAGE_MARGIN, height - 0.42 * inch, brand["name"])

    canvas.setFont('Helvetica', 7)
    canvas.setFillColor(brand["text_secondary"])
    canvas.drawString(PAGE_MARGIN, 0.4 * inch, brand["footer"])
    canvas.drawRightString(width - PAGE_MARGIN, 0.4 * inch, f'Page {doc.page}')

    canvas.setStrokeColor(brand["border"])
    canvas.setLineWidth(0.5)
    canvas.line(PAGE_MARGIN, 0.55 * inch, width - PAGE_MARGIN, 0.55 * inch)

    canvas.restoreState()


def render_brief(md_path, brand, logo_path=None, output_path=None):
    if not os.path.exists(md_path):
        print(f"Error: {md_path} not found", file=sys.stderr)
        sys.exit(1)

    with open(md_path, 'r') as f:
        md_text = f.read()

    if not output_path:
        output_path = os.path.splitext(md_path)[0] + '.pdf'

    styles = build_styles(brand)
    blocks = parse_markdown(md_text)
    brand_hex = brand["primary"].hexval() if hasattr(brand["primary"], 'hexval') else "#37474F"

    story = []
    story.append(Spacer(1, 0.3 * inch))

    for block in blocks:
        btype = block['type']

        if btype == 'h1':
            story.append(Paragraph(inline_markup(block['text'], brand_hex), styles['Brief_Title']))
            story.append(Spacer(1, 4))

        elif btype == 'h2':
            story.append(Spacer(1, 4))
            story.append(HRFlowable(width='100%', thickness=1, color=brand["primary"],
                                    spaceAfter=4, spaceBefore=8))
            story.append(Paragraph(inline_markup(block['text'], brand_hex), styles['Brief_H2']))

        elif btype == 'h3':
            story.append(Paragraph(inline_markup(block['text'], brand_hex), styles['Brief_H3']))

        elif btype == 'hr':
            story.append(HRFlowable(width='100%', thickness=0.5, color=brand["border"],
                                    spaceAfter=8, spaceBefore=8))

        elif btype == 'verdict':
            story.append(Paragraph(inline_markup(block['text'], brand_hex), styles['Brief_Verdict']))

        elif btype == 'blockquote':
            story.append(Paragraph(inline_markup(block['text'], brand_hex), styles['Brief_Verdict']))

        elif btype == 'paragraph':
            story.append(Paragraph(inline_markup(block['text'], brand_hex), styles['Brief_Body']))

        elif btype == 'bullets':
            for item in block['items']:
                story.append(Paragraph(
                    f'<bullet>&bull;</bullet> {inline_markup(item, brand_hex)}',
                    styles['Brief_Bullet']
                ))

        elif btype == 'numbered':
            for idx, item in enumerate(block['items'], 1):
                story.append(Paragraph(
                    f'<bullet>{idx}.</bullet> {inline_markup(item, brand_hex)}',
                    styles['Brief_Bullet']
                ))

        elif btype == 'code':
            # Wrap long code lines
            code_text = block['text'].replace('&', '&amp;').replace('<', '&lt;').replace('>', '&gt;')
            code_text = code_text.replace('\n', '<br/>')
            story.append(Paragraph(code_text, styles['Brief_Code']))

        elif btype == 'table':
            t = build_pdf_table(block['lines'], styles, brand)
            if t:
                story.append(Spacer(1, 4))
                story.append(t)
                story.append(Spacer(1, 8))

    doc = SimpleDocTemplate(
        output_path, pagesize=letter,
        leftMargin=PAGE_MARGIN, rightMargin=PAGE_MARGIN,
        topMargin=0.7 * inch, bottomMargin=0.7 * inch
    )

    def on_page(canvas, doc):
        header_footer(canvas, doc, brand, logo_path)

    doc.build(story, onFirstPage=on_page, onLaterPages=on_page)
    print(f"PDF saved: {output_path}")
    return output_path


if __name__ == '__main__':
    parser = argparse.ArgumentParser(description='Render research brief to branded PDF')
    parser.add_argument('brief_path', help='Path to BRIEF.md')
    parser.add_argument('--brand', choices=list(BRANDS.keys()), default='research',
                        help='Brand preset (default: research)')
    parser.add_argument('--brand-color', help='Override primary brand color (hex)')
    parser.add_argument('--brand-name', help='Override brand name')
    parser.add_argument('--logo', help='Path to logo PNG')
    parser.add_argument('--output', '-o', help='Output PDF path')
    args = parser.parse_args()

    brand = get_brand(args)
    render_brief(args.brief_path, brand, logo_path=args.logo, output_path=args.output)
